years = range(1988, 2025)

for y in years:
    print(f"WB {y}")

    f_et0 = ET0_DIR / f"commune_monthly_et0_{y}.parquet"
    f_pr  = PRECIP_DIR / f"commune_monthly_precip_{y}.parquet"

    df_et0 = pd.read_parquet(f_et0)
    df_pr  = pd.read_parquet(f_pr)

    df = (
        df_pr.merge(
            df_et0,
            on=["code_insee", "time"],
            how="inner"
        )
        .rename(columns={
            "precip_mm": "P_mm",
            "et0_mm": "ET0_mm"
        })
    )

    df["D_mm"] = df["P_mm"] - df["ET0_mm"]

    outp = OUT_DIR / f"commune_monthly_water_balance_{y}.parquet"
    df[["code_insee", "time", "D_mm"]].to_parquet(
        outp, index=False, compression="snappy"
    )

    print(
        "✔ communes:", df["code_insee"].nunique(),
        "NaN:", float(df["D_mm"].isna().mean())
    )

