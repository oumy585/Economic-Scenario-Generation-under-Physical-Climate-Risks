import pandas as pd
import numpy as np
from pathlib import Path
from scipy.stats import genlogistic, norm

WB_DIR  = Path("/content/safran_communes_WB_monthly")
OUT_DIR = Path("/content/spei_communes")
OUT_DIR.mkdir(exist_ok=True)

K_LIST = [3, 6, 12]
YEARS  = range(1988, 2025)

# --- lecture WB complet ---
dfs = []
for y in YEARS:
    dfs.append(pd.read_parquet(WB_DIR / f"commune_monthly_water_balance_{y}.parquet"))
df = pd.concat(dfs, ignore_index=True)

df["time"] = pd.to_datetime(df["time"])
df = df.sort_values(["code_insee", "time"])

# --- calcul SPEI-k ---
for k in K_LIST:
    print(f"\n=== SPEI-{k} ===")

    df[f"D_k{k}"] = (
        df.groupby("code_insee")["D_mm"]
          .transform(lambda s: s.rolling(k, min_periods=k).sum())
    )

    out_rows = []

    for code, g in df.groupby("code_insee"):
        x = g[f"D_k{k}"].dropna()
        if len(x) < 30:   # seuil standard
            continue

        # Ajustement loi log-logistique (SPEI standard)
        c, loc, scale = genlogistic.fit(x.values)

        cdf = genlogistic.cdf(g[f"D_k{k}"], c, loc=loc, scale=scale)
        spei = norm.ppf(cdf)

        tmp = g[["code_insee", "time"]].copy()
        tmp[f"SPEI_{k}"] = spei
        out_rows.append(tmp)

    spei_df = pd.concat(out_rows, ignore_index=True)

    outp = OUT_DIR / f"SPEI_{k}_communes_1988_2024.parquet"
    spei_df.to_parquet(outp, index=False, compression="snappy")

    print(
        "✔ communes :", spei_df["code_insee"].nunique(),
        "| NaN :", float(spei_df[f"SPEI_{k}"].isna().mean())
    )

