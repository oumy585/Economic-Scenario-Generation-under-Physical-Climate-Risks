NC_IN = f"{DIR_FAO}/FAO56_climate_base_monthly_1988_2024.nc"
ds = xr.open_dataset(NC_IN)

# Minimal physical  adjustements
# ============================================================
Rnl = xr.where(ds["Rnl_MJm2day"] < 0, 0.0, ds["Rnl_MJm2day"])
Rn  = ds["Rns_MJm2day"] - Rnl
Rs  = xr.where(ds["Rs_MJm2day"] > ds["Rso_MJm2day"],
               ds["Rso_MJm2day"], ds["Rs_MJm2day"])

ds = ds.assign(
    Rnl_MJm2day=Rnl,
    Rn_MJm2day=Rn,
    Rs_MJm2day=Rs
)

# ============================================================
# 3. ET0 FAO-56 Monthly (FORMULa ALLEN)
# ============================================================

T  = ds["Tmean_C"]
u2 = ds["Wind2m_mean_ms"]
es = ds["es_kPa"]
ea = ds["ea_kPa"]
Rn = ds["Rn_MJm2day"]
P  = ds["Pressure_kPa"]

# Soil heat flux(Monthly)
G = 0.0

# Slope of the saturation vapor pressure curve (kPa/°C)
Delta = 4098 * (0.6108 * np.exp(17.27 * T / (T + 237.3))) / (T + 237.3)**2

# Psychrometric constant (kPa/°C)
gamma = 0.000665 * P

# ET0 en mm/day (FAO-56 Allen and al.)
ET0_mmday = (
    (0.408 * Delta * (Rn - G)
     + gamma * (900.0 / (T + 273.0)) * u2 * (es - ea))
    /
    (Delta + gamma * (1.0 + 0.34 * u2))
).rename("ET0_mmday")

ET0_mmday.attrs["units"]  = "mm/day"
ET0_mmday.attrs["method"] = "FAO-56 Allen et al. (monthly, G=0)"

# Conversion to mm/month
days = ds["time"].dt.days_in_month
ET0_mm_month = (ET0_mmday * days).rename("ET0_mm_month")
ET0_mm_month.attrs["units"] = "mm/month"

# add to dataset
ds = ds.assign(
    ET0_mmday=ET0_mmday,
    ET0_mm_month=ET0_mm_month
)

#  NaN Contrôle (xarray → isnull)
print("NaN ET0 mensuel :", float(ds["ET0_mm_month"].isnull().mean()))

NC_OUT = f"{DIR_FAO}/FAO56_climate_base_monthly_1988_2024_ET0.nc"
ds.to_netcdf(NC_OUT)
ds.close()

